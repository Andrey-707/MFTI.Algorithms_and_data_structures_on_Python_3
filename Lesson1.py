# Цикл While

# Итерация - это однократное выполнение тела цикла
w = str(input("Какая сейчас погода: "))
i = "дождь"
u = "Иду гулять"
while i != w:
    print(u)
    w = str(input("Какая сейчас погода: "))
else:
    print("Иду учиться")

# Цикл While пример логина в какой-либо системе.
quess = input("Enter password: ")
password = "Andrey"
while quess != password:
    print("Invalid password!")
    quess = input("Enter password: ")
else:
    print("Welcome!")

