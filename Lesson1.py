# Функция if -> else
print("Введите число a: ")
a = int(input())
if a > 10:
    print("Введенное число > 10")
elif a > 5 and a < 10:
    print("Введенное число в пределах от 5 до 10")
elif a == 10:
    print("Введенное число = 10")
else:
    print("Введенное число меньше 5")

